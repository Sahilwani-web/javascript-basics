// while

let index = 0
while (index<=10) {
    console.log(`value is ${index}`)
    index = index + 2;
};

let myArr = ["sahil", "Mughes", "Bintu"]
let arr = 0
while (arr < myArr.length) {
    console.log(`value is ${myArr[arr]}`);
    arr++
};

